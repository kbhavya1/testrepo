                <h1 class="page-title">Paper Submission Guidelines</h1>                                                  <div class="region region-content">
                    <div id="block-system-main" class="block block-system">


                        <div class="content">
                            <span property="dc:title" content="Paper Submission Guidelines" class="rdf-meta element-hidden"></span><span property="sioc:num_replies" content="0" datatype="xsd:integer" class="rdf-meta element-hidden"></span>


                            <div class="content">
                                <div class="field field-name-body field-type-text-with-summary field-label-hidden"><div class="field-items"><div class="field-item even" property="content:encoded"><div class="rtejustify"><span style="font-size:16px">Prospective authors are invited to submit manuscripts reporting original unpublished research and recent developments in the topics related to the conference. It is required that the manuscript follows the standard IEEE camera-ready format (IEEE standard format, double column, 10-point font). Submissions must include title, abstract, keywords, author and affiliation with email address. Please double-check the paper size in your page setup to make sure you are using the letter-size paper layout (8.5 inch X 11 inch). The paper should not contain page numbers or any special headers or footers.</span></div>
                                            <div class="rtejustify"> </div>
                                            <div><span style="font-size:16px">The IEEE templates in Microsoft Word (US Letter) and LaTeX format can be found at :<br /><a href="http://www.ieee.org/conferences_events/conferences/publishing/templates.html" target="_blank">http://www.ieee.org/conferences_events/conferences/publishing/templates.html</a></span></div>
                                            <div> </div>
                                            <div><span style="font-size:16px"><strong>ICACCI Paper Categories </strong>(7 pages maximum -3 additional pages allowed but at an extra charge):</span></div>
                                            <ul><li>
                                                    <div><span style="font-size:16px">Regular Paper </span></div>
                                                </li>
                                                <li>
                                                    <div><span style="font-size:16px">Short Paper (Work-in-Progress) </span></div>
                                                </li>
                                                <li>
                                                    <div><span style="font-size:16px">Poster Paper</span></div>
                                                </li>
                                            </ul><div class="rtejustify"><span style="font-size:16px">Regular papers should present novel perspectives within the general scope of the conference. Short papers (Work-in-Progress) are an opportunity to present preliminary or interim results. Posters are intended for ongoing research projects, concrete realizations, or industrial applications/projects presentations. <strong>The conference only accepts full manuscripts with minimum 3 pages. Manuscripts with less than 3 pages will not be considered for review. </strong></span></div>
                                            <div class="rtejustify"> </div>
                                            <div class="rtejustify"><span style="font-size:16px">Literature reviews/survey papers will only be considered if they present a new perspective or clearly benefit the field. To be published, such papers must go beyond a review of the literature to define the field in a new way or highlight exciting new technologies or areas of research.</span></div>
                                            <div class="rtejustify"> </div>
                                            <div class="rtecenter"><span style="font-size:16px"><a href="http://edas.info/N24343" target="_blank"><img alt="paper_sub_button.jpg" src="http://icacci-conference.org/website/sites/default/files/paper_sub_button.jpg" /></a></span></div>
                                            <div class="rtejustify"><span style="font-size:16px">Authors should submit their papers online. We use EDAS system for submission of papers and review process. Unregistered authors should first create an account on EDAS to log on. Detailed usage instruction on EDAS can be found <strong><a href="http://edas.info/doc/authors.html" target="_blank">here</a></strong>. To avoid last minutes network problems, authors are strongly encouraged to create their EDAS submission system user accounts (if you don’t already have one) and register their submission (the paper’s author(s), title and abstract) well in advance of the deadline.</span></div>
                                            <div class="rtejustify"> </div>
                                            <div class="rtejustify"><span style="font-size:16px">All submitted papers will be subjected to a "similarity test" by EDAS paper similarity checker (iThenticate). Papers achieving a high similarity score will be examined and those that are deemed unacceptable will be rejected/withdrawn without a formal review.</span></div>
                                            <div class="rtejustify"> </div>
                                            <div class="rtejustify"><span style="font-size:16px">The manuscripts should be submitted in PDF format. Please compare all author names in EDAS with the author list in your paper. They must be identical and in the same order.</span></div>
                                            <div class="rtejustify"> </div>
                                            <div class="rtejustify"><span style="font-size:16px">The ICACCI organizers regard plagiarism as a serious professional misconduct. All submissions will be screened for plagiarism and when identified, the submissions by the same author will be rejected. A submission SHOULD NOT be intended to get reviews from the Program Committee for the sole purpose of improving the quality of the paper. A submission implies that the author intends to ultimately register the paper upon a favorable response from the conference organizers.  ICACCI doesn’t encourage withdrawals after the paper is accepted.</span></div>
                                            <div class="rtejustify"> </div>
                                            <div class="rtejustify"><span style="font-size:16px">The papers under regular and work-in-progress categories will be scheduled for oral presentation. Posters will be presented during sessions reserved for posters (Each poster session has two parts - a short oral presentation followed by the display of poster). Accepted and presented papers will be published in the conference proceedings and submitted to IEEE Xplore as well as other Abstracting and Indexing (A&amp;I) databases. </span></div>
                                            <div class="rtejustify"> </div>
                                            <div class="rtejustify"><span style="font-size:16px"><span style="color:#0000CD">
<!--<strong>Accepted and presented papers will be published in the conference proceedings and submitted to IEEE Xplore as well as other Abstracting and Indexing (A&amp;I) databases.​&nbsp;</strong>--><p></p></span>Independently of the presentation type, all papers are included in the proceedings. To be published in the ICACCI 2018 Conference Proceedings, an author of an accepted paper is required to register for the conference at the full rate. <strong>All accepted papers MUST be presented at the conference by one of the authors, or, if none of the authors are able to attend, by a qualified surrogate.</strong></span></div>
